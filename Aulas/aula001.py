n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
print(type(n1))
print(type(n2))

s = n1 + n2
m = n1 * n2
print(f'A soma entre {n1} e {n2} é {s}')
print(f'A multiplicação entre {n1} e {n2} é {m}')

